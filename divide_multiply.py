"""
returns num divided by two
:param num: int.
:return: int quotient of num divided by 2
"""
def divide_by_two(num):
    return(num // 2)

"""
returns num multiplied by four
:param num: int.
:return: int result of num multiplied by 4
"""
def multiply_by_four(num):
    return(num * 4)


division = divide_by_two(10)

multiply_by_four(division)
